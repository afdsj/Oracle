-- 주제 선정
-- 대표의 직급 조회
SELECT E.EMP_NAME,
       J.JOB_NAME
  FROM EMPLOYEE E
  JOIN JOB J USING (JOB_CODE)
 WHERE EMP_NAME = '강소임';
  
-- 이상우 직원의 월급 조회
SELECT EMP_NAME,
       SALARY
  FROM EMPLOYEE
 WHERE EMP_NAME = '이상우';
 
-- 전지환 직원의 판매현황 조회
SELECT
        E.EMP_NAME,
        S.*
   FROM SALES S
   JOIN EMPLOYEE E ON (E.EMP_ID = S.EMP_ID)
  WHERE EMP_NAME = '전지환';

-- 모든 직원 출퇴근 현항 조회
SELECT 
      V.VEH_NAME AS 교통수단,
      (SELECT COUNT(*)
         FROM EMPLOYEE E
        WHERE E.VEH_CODE = V.VEH_CODE) AS 인원
  FROM VEHICLE V;
  
-- 연차가 많은 직원수 조회
SELECT
        E.EMP_NAME,
        E.ANNUAL
   FROM EMPLOYEE E
  WHERE ANNUAL = (SELECT MAX(E2.ANNUAL) FROM EMPLOYEE E2);