CREATE SEQUENCE P_NUMBER
START WITH 1
INCREMENT BY 1
MAXVALUE 40
NOCYCLE
NOCACHE;

drop sequence p_number;

-- 시퀀스 NEXTVAL 1회 수행 후 실행 가능
SELECT P_NUMBER.CURRVAL FROM DUAL;

-- 시퀀스 조회
SELECT *
FROM ALL_SEQUENCES
WHERE SEQUENCE_NAME = 'P_NUMBER';

-- 시퀀스 삭제
DROP SEQUENCE P_NUMBER;




